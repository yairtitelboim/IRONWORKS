/**
 * Normalize city name for lookup
 */
export function normalizeCityName(city) {
  if (!city) return null;
  return city.trim().replace(/\s+/g, ' ');
}

/**
 * Get county from city name
 */
export function getCountyFromCity(city) {
  if (!city) return null;
  const normalized = normalizeCityName(city);
  return TEXAS_CITY_TO_COUNTY[normalized] || null;
}

/**
 * Check if a city exists in our database
 */
export function hasCityMapping(city) {
  if (!city) return false;
  const normalized = normalizeCityName(city);
  return TEXAS_CITY_TO_COUNTY.hasOwnProperty(normalized);
}

